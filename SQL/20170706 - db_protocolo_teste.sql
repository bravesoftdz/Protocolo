/* Definition for the `view_hist_processos` view : */

CREATE ALGORITHM=UNDEFINED DEFINER='root'@'%' SQL SECURITY DEFINER VIEW `view_hist_processos`
AS
select
  `cp`.`id_cad_protocolo` AS `id_cad_protocolo`,
  `hp`.`id_historico_processo` AS `id_historico_processo`,
  `cp`.`flag_arquivado` AS `flag_arquivado`,
  `hp`.`dt_tramitacao` AS `dt_tramitacao`,
  `hp`.`Obs` AS `Obs`,
  `us`.`nome` AS `Nome`,
  `dp`.`departamento` AS `departamento`,
  (case
     when (`dph`.`departamento` is not null) then `dph`.`departamento`
     else `dp`.`departamento`
   end) AS `Hist_departamento`,
  `cp`.`dt_cadastro` AS `dt_cadastro`,
  `cp`.`num_processo` AS `num_processo`,
  `cp`.`dt_processo` AS `dt_processo`,
  `cp`.`hr_processo` AS `hr_processo`,
  `cp`.`Assunto` AS `Assunto`,
  `cp`.`Obs` AS `Obs_Geral`,
  `cp`.`interessado` AS `interessado`,
  `cp`.`pasta` AS `pasta`,
  `cp`.`num_oficcio` AS `num_oficcio`,
  `hp`.`id_usuario` AS `id_usuario`,
  `hp`.`id_departamento` AS `id_departamento`,
  `hp`.`is_recebido` AS `is_recebido`
from
  ((((`tb_cad_protocolo` `cp`
  left join `tb_historico_processo` `hp` on ((`hp`.`id_cad_protocolo` = `cp`.`id_cad_protocolo`)))
  left join `tb_usuario` `us` on ((`us`.`id_usuario` = `hp`.`id_usuario`)))
  join `tb_departamento` `dp` on ((`dp`.`id_departamento` = `cp`.`id_departamento`)))
  left join `tb_departamento` `dph` on ((`dph`.`id_departamento` = `hp`.`id_departamento`)))
order by
  `hp`.`dt_tramitacao` desc;

/* Definition for the `view_lista_processos` view : */

CREATE ALGORITHM=UNDEFINED DEFINER='root'@'%' SQL SECURITY DEFINER VIEW `view_lista_processos`
AS
select
  `cp`.`id_cad_protocolo` AS `id_cad_protocolo`,
  ''  AS `NOME`,
  (
    select
      `hp`.`Hist_departamento`
    from
      `view_hist_processos` `hp`
    where
      (`hp`.`id_cad_protocolo` = `cp`.`id_cad_protocolo`)
    limit
      1
  ) AS `Departamento`,
  `cp`.`dt_cadastro` AS `DT_CADASTRO`,
  `cp`.`flag_arquivado` AS `FLAG_ARQUIVADO`,
  `cp`.`num_processo` AS `NUM_PROCESSO`,
  (
    select
      `hp`.`dt_tramitacao`
    from
      `view_hist_processos` `hp`
    where
      (`hp`.`id_cad_protocolo` = `cp`.`id_cad_protocolo`)
    limit
      1
  ) AS `DT_PROCESSO`,
  (
    select
      `hp`.`is_recebido`
    from
      `view_hist_processos` `hp`
    where
      (`hp`.`id_cad_protocolo` = `cp`.`id_cad_protocolo`)
    limit
      1
  ) AS `is_recebido`,
  `cp`.`hr_processo` AS `HR_PROCESSO`,
  `cp`.`Assunto` AS `ASSUNTO`,
  `cp`.`Obs` AS `OBS`,
  `cp`.`interessado` AS `INTERESSADO`,
  `cp`.`num_oficcio` AS `NUM_OFICCIO`
from
  `tb_cad_protocolo` `cp`
order by
  (
    select
      `hp`.`dt_tramitacao`
    from
      `view_hist_processos` `hp`
    where
      (`hp`.`id_cad_protocolo` = `cp`.`id_cad_protocolo`)
    limit
      1
  ) desc,
  `cp`.`num_processo`;

/* Definition for the `view_log` view : */

CREATE ALGORITHM=UNDEFINED DEFINER='root'@'%' SQL SECURITY DEFINER VIEW `view_log`
AS
select
  `l`.`id_usuario` AS `id_usuario`,
  `l`.`id_log` AS `ID_LOG`,
  `u`.`usuario` AS `USUARIO`,
  `u`.`nome` AS `NOME`,
  `a`.`acao` AS `ACAO`,
  `l`.`Form` AS `FORM`,
  `l`.`IP` AS `IP`,
  `l`.`dt_acao` AS `DT_ACAO`,
  `l`.`hr_acao` AS `HR_ACAO`
from
  ((`tb_log` `l`
  join `tb_acao` `a` on ((`a`.`id_acao` = `l`.`id_acao`)))
  join `tb_usuario` `u` on ((`u`.`id_usuario` = `l`.`id_usuario`)))
where
  ((`l`.`id_usuario` <> 2484) and
  (`l`.`id_usuario` <> 1));

/* Definition for the `view_resumo_por_departamento` view : */

CREATE ALGORITHM=UNDEFINED DEFINER='root'@'%' SQL SECURITY DEFINER VIEW `view_resumo_por_departamento`
AS
select
  `view_lista_processos`.`Departamento` AS `DEPARTAMENTO`,
  count(0) AS `Total`
from
  `view_lista_processos`
group by
  `view_lista_processos`.`Departamento`
order by
  `view_lista_processos`.`Departamento`;

/* Definition for the `view_usuarios` view : */

CREATE ALGORITHM=UNDEFINED DEFINER='root'@'%' SQL SECURITY DEFINER VIEW `view_usuarios`
AS
select
  `us`.`id_usuario` AS `id_usuario`,
  `us`.`id_unidade` AS `id_unidade`,
  `dp`.`departamento` AS `departamento`,
  `us`.`nome` AS `nome`,
  `us`.`usuario` AS `usuario`,
  `us`.`senha` AS `senha`,
  `us`.`is_adm` AS `is_adm`,
  `us`.`eMail` AS `eMail`,
  `us`.`Is_Bloqueado` AS `Is_Bloqueado`
from
  (`tb_usuario` `us`
  join `tb_departamento` `dp` on ((`dp`.`id_departamento` = `us`.`id_unidade`)))
order by
  `us`.`nome`;

